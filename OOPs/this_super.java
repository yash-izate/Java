/*
 * Theory - this
 * 
 * this Keyword
 * this refers to the current instance of the class. It is used to:
 * 
 * Differentiate between instance variables and parameters when they have the
 * same name.
 * Call another constructor in the same class (constructor chaining).
 * Pass the current instance as an argument to another method.
 */

 /*
 * Theory - super
 * 
 * super Keyword
 * super refers to the superclass (parent class) instance. It is used to:
 * 
 * Call the superclass constructor.
 * Access superclass methods that have been overridden in the subclass.
 * Access superclass fields if they are hidden by fields in the subclass.
 */