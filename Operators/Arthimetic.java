package Operators;

public class Arthimetic {
  public static void main(String[] args) {

    int a = 10;
    float b = 3.14f;

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a / b);
    System.out.println(a * b);
    System.out.println(a % b);

    double x = 54.654d;
    long y = 100l;

    System.out.println(x + y);
    System.out.println(x - y);
    System.out.println(x / y);
    System.out.println(x * y);
    System.out.println(x % y);

  }

}
