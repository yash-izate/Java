package Basics;

public class EscapeSequences {
  public static void main(String[] args) {

    // \n next line after this
    System.out.println("My name is Yash. \nMy age is 21.");

    // \t inserts a tab in the text at this point
    System.out.println("I live in\tIndia");

    // \b inserts a backspace in the text at this point (n is removed)
    System.out.println("Yeshwantrao Chavan\b College of Engineering");

    // \\ - inserts a backlash
    System.out.println("Where is address? 150\\2 Bus Stand");

    // \" - inserts a double quote character
    System.out.println("What is going in \"Class\" ");

    // \' - inserts a single quote character
    System.out.println("Hello \'Kitty\' ");

  }

}
